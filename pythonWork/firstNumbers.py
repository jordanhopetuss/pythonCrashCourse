# Using the range() function
for value in range(1, 6):
    print(value)

# using rangee() function to make a list of numbers
# 1 is the start
# 6 is the stop
numbers = list(range(1, 6))
print(numbers)
# 2 is the step value it repeats every 2 positions in a list
oddNumbers = list(range(1, 6, 2))
print(oddNumbers)

evenNumbers = list(range(2, 11, 2))
print(evenNumbers)
